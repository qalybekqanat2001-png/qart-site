def format_date_human(iso_date: str) -> str:
    """'2026-07-12' -> '12 июля 2026 г.'"""
    try:
        d = _date.fromisoformat(iso_date)
        return f"{d.day} {RU_MONTHS[d.month]} {d.year} г."
    except (ValueError, KeyError):
        return iso_date


